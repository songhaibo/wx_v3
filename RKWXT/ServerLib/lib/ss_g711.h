extern const SS_UINT8  g_ss_g711_linear2ulaw_tab[16384];
extern const SS_UINT8  g_ss_g711_linear2alaw_tab[16384];
extern const SS_UINT16 g_ss_g711_ulaw2linear_tab[256];
extern const SS_UINT16 g_ss_g711_alaw2linear_tab[256];



/**
 * Convert 16-bit linear PCM value to 8-bit A-Law.
 *
 * @param pcm_val   16-bit linear PCM value.
 * @return        8-bit A-Law value.
 */
#define ss_audio_g711_linear2alaw(pcm_val) g_ss_g711_linear2alaw_tab[(((SS_UINT16)pcm_val) >> 2) & 0x3fff]

/**
 * Convert 8-bit A-Law value to 16-bit linear PCM value.
 *
 * @param chara_val 8-bit A-Law value.
 * @return        16-bit linear PCM value.
 */
#define ss_audio_g711_alaw2linear(chara_val) g_ss_g711_alaw2linear_tab[chara_val]

/**
 * Convert 16-bit linear PCM value to 8-bit U-Law.
 *
 * @param pcm_val   16-bit linear PCM value.
 * @return        U-bit A-Law value.
 */
#define ss_audio_g711_linear2ulaw(pcm_val) g_ss_g711_linear2ulaw_tab[(((SS_UINT16)pcm_val) >> 2) & 0x3fff]

/**
 * Convert 8-bit U-Law value to 16-bit linear PCM value.
 *
 * @param u_val        8-bit U-Law value.
 * @return        16-bit linear PCM value.
 */
#define ss_audio_g711_ulaw2linear(u_val) g_ss_g711_ulaw2linear_tab[u_val]

/**
 * Convert 8-bit A-Law value to 8-bit U-Law value.
 *
 * @param aval        8-bit A-Law value.
 * @return        8-bit U-Law value.
 */
#define ss_audio_g711_alaw2ulaw(aval) ss_audio_g711_linear2ulaw(ss_audio_g711_alaw2linear(aval))

/**
 * Convert 8-bit U-Law value to 8-bit A-Law value.
 *
 * @param uval        8-bit U-Law value.
 * @return        8-bit A-Law value.
 */
#define ss_audio_g711_ulaw2alaw(uval) ss_audio_g711_linear2alaw(ss_audio_g711_ulaw2linear(uval))



